`timescale 1ns / 1ps

module tb_lab11_task1;

    // ??? DUT signals ????????????????????????????????????????????????????????
    reg         clk, reset;
    reg  [31:0] instruction;
    reg         PCSrc;

    wire [31:0] pc, pc_plus4, branch_target, pc_next, imm_out;

    // ??? Instantiations ?????????????????????????????????????????????????????
    ProgramCounter DUT_PC (
        .clk     (clk),
        .reset   (reset),
        .pc_next (pc_next),
        .pc      (pc)
    );

    pcAdder DUT_pcAdd (
        .pc       (pc),
        .pc_plus4 (pc_plus4)
    );

    immGen DUT_immGen (
        .instruction (instruction),
        .imm_out     (imm_out)
    );

    branchAdder DUT_brAdd (
        .pc            (pc),
        .imm           (imm_out),
        .branch_target (branch_target)
    );

    mux2 DUT_mux (
        .in0 (pc_plus4),
        .in1 (branch_target),
        .sel (PCSrc),
        .out (pc_next)
    );

    // ??? Clock generation ????????????????????????????????????????????????????
    initial clk = 0;
    always #5 clk = ~clk;   // 10 ns period = 100 MHz

    // ??? Task: print current state ???????????????????????????????????????????
    task print_state;
        input [63:0] test_id;
        begin
            $display("Test %-20d | PC=%08h  PC+4=%08h  Branch=%08h  PCSrc=%b  NextPC=%08h  IMM=%08h (%0d)",
                     test_id, pc, pc_plus4, branch_target, PCSrc, pc_next, imm_out, $signed(imm_out));
        end
    endtask

    // ??? Stimulus ????????????????????????????????????????????????????????????
    initial begin
        $display("============================================================");
        $display("  Lab 11 Task 1 - PC / ImmGen / Branch Infrastructure TB");
        $display("============================================================");

        // ---------- Reset ----------
        reset = 1; PCSrc = 0;
        // NOP (addi x0,x0,0) - I-type, imm = 0
        instruction = 32'h00000013;
        @(posedge clk); #1;
        reset = 0;

        // ????????????????????????????????????????????????????
        //  TEST 1-5: Sequential execution (PCSrc = 0)
        //  PC should increment by 4 each cycle
        // ????????????????????????????????????????????????????
        $display("\n--- Sequential execution (PCSrc=0) ---");
        PCSrc = 0;

        @(posedge clk); #1;
        print_state(1);
        if (pc !== 32'h00000004) $display("  *** FAIL: expected 0x00000004, got %08h", pc);
        else $display("  PASS");

        @(posedge clk); #1;
        print_state(2);
        if (pc !== 32'h00000008) $display("  *** FAIL: expected 0x00000008, got %08h", pc);
        else $display("  PASS");

        @(posedge clk); #1;
        print_state(3);
        if (pc !== 32'h0000000C) $display("  *** FAIL: expected 0x0000000C, got %08h", pc);
        else $display("  PASS");

        @(posedge clk); #1;
        print_state(4);
        if (pc !== 32'h00000010) $display("  *** FAIL: expected 0x00000010, got %08h", pc);
        else $display("  PASS");

        @(posedge clk); #1;
        print_state(5);
        if (pc !== 32'h00000014) $display("  *** FAIL: expected 0x00000014, got %08h", pc);
        else $display("  PASS");

        // ????????????????????????????????????????????????????
        //  TEST 6: I-type immediate  (addi x1, x0, -5)
        //  inst = imm[11:0]=0xFFB | rs1=x0 | funct3=000 | rd=x1 | opcode=0010011
        //  Expected imm = -5 = 0xFFFFFFFB
        // ????????????????????????????????????????????????????
        $display("\n--- I-type immediate: addi x1,x0,-5 ---");
        instruction = 32'hFFB00093;   // addi x1, x0, -5
        PCSrc = 0;
        @(posedge clk); #1;
        print_state(6);
        if (imm_out !== 32'hFFFFFFFB)
            $display("  *** FAIL: I-type imm expected 0xFFFFFFFB, got %08h", imm_out);
        else
            $display("  PASS - I-type imm = %0d (0x%08h)", $signed(imm_out), imm_out);

        // ????????????????????????????????????????????????????
        //  TEST 7: I-type immediate  (lw x2, 8(x1))
        //  imm = 8 = 12'h008
        //  Expected imm = 32'h00000008
        // ????????????????????????????????????????????????????
        $display("\n--- I-type immediate: lw x2, 8(x1) ---");
        instruction = 32'h00808103;   // lw x2, 8(x1)
        PCSrc = 0;
        @(posedge clk); #1;
        print_state(7);
        if (imm_out !== 32'h00000008)
            $display("  *** FAIL: expected 0x00000008, got %08h", imm_out);
        else
            $display("  PASS - I-type imm = %0d", $signed(imm_out));

        // ????????????????????????????????????????????????????
        //  TEST 8: S-type immediate  (sw x2, -4(x1))
        //  imm = -4  => inst[31:25]=7'b1111111, inst[11:7]=5'b11100
        //  Expected imm = 0xFFFFFFFC
        // ????????????????????????????????????????????????????
        $display("\n--- S-type immediate: sw x2,-4(x1) ---");
        instruction = 32'hFE20AE23;   // sw x2, -4(x1)
        PCSrc = 0;
        @(posedge clk); #1;
        print_state(8);
        if (imm_out !== 32'hFFFFFFFC)
            $display("  *** FAIL: S-type imm expected 0xFFFFFFFC, got %08h", imm_out);
        else
            $display("  PASS - S-type imm = %0d (0x%08h)", $signed(imm_out), imm_out);

        // ????????????????????????????????????????????????????
        //  TEST 9: B-type immediate  (beq x1, x2, +8)
        //  Branch offset = +8 bytes  =>  imm field stores 8>>1 = 4
        //  Encoding: inst[31]=0, inst[7]=0, inst[30:25]=000001, inst[11:8]=0000
        //  Expected imm_out = 32'h00000008
        //  Expected branch_target = current_PC + (8>>1 <<1) = current_PC + 8
        // ????????????????????????????????????????????????????
        $display("\n--- B-type: beq x1,x2,+8 (PCSrc=1) ---");
        instruction = 32'h00208463;   // beq x1, x2, +8
        PCSrc = 1;
        @(posedge clk); #1;
        print_state(9);
        if (imm_out !== 32'h00000008)
            $display("  *** FAIL: B-type imm expected 0x00000008, got %08h", imm_out);
        else
            $display("  PASS - B-type imm = %0d", $signed(imm_out));

        // ????????????????????????????????????????????????????
        //  TEST 10: Branch taken - verify PC jumps to branch_target
        // ????????????????????????????????????????????????????
        $display("\n--- Branch taken: PC should update to branch_target ---");
        @(posedge clk); #1;
        print_state(10);
        if (pc !== 32'h00000040)
            $display("  *** FAIL: expected PC=00000040, got %08h", pc);
        else
            $display("  PASS - PC correctly jumped to branch target");

        // ????????????????????????????????????????????????????
        //  TEST 11: Back to sequential after branch (PCSrc=0)
        // ????????????????????????????????????????????????????
        $display("\n--- Sequential again (PCSrc=0) ---");
        PCSrc = 0;
        instruction = 32'h00000013;  // NOP
        @(posedge clk); #1;
        print_state(11);
        if (pc !== 32'h00000044)
            $display("  *** FAIL: expected PC=00000044, got %08h", pc);
        else
            $display("  PASS - Sequential increment resumed");

        $display("\n============================================================");
        $display("  Simulation complete.");
        $display("============================================================");
        $finish;
    end

    // ??? Optional VCD dump ???????????????????????????????????????????????????
    initial begin
        $dumpfile("lab11_task1.vcd");
        $dumpvars(0, tb_lab11_task1);
    end

endmodule